import "package:flutter/material.dart";
import "package:photos/service_locator.dart";
import "package:photos/services/wrapped/wrapped_state_service.dart";
import "package:photos/theme/ente_theme.dart";

class WrappedDiscoverySection extends StatelessWidget {
  const WrappedDiscoverySection({
    required this.state,
    super.key,
  });

  final WrappedEntryState state;

  @override
  Widget build(BuildContext context) {
    final colorScheme = getEnteColorScheme(context);
    final textTheme = getEnteTextTheme(context);

    final bool hasProgress =
        state.resumeIndex > 0 && state.resumeIndex < (state.result?.cards.length ?? 0);
    final String subtitle = state.isComplete
        ? "Replay the full story."
        : hasProgress
            ? "Continue watching from card ${state.resumeIndex + 1}."
            : "Autogenerated highlights from your 2025.";

    return Padding(
      padding: const EdgeInsets.fromLTRB(12, 16, 12, 32),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            "Wrapped 2025",
            style: textTheme.largeBold,
          ),
          const SizedBox(height: 12),
          Material(
            color: colorScheme.backgroundElevated,
            borderRadius: BorderRadius.circular(18),
            child: InkWell(
              borderRadius: BorderRadius.circular(18),
              onTap: () => _handleTap(context),
              child: Padding(
                padding:
                    const EdgeInsets.symmetric(horizontal: 16, vertical: 18),
                child: Row(
                  children: [
                    Icon(
                      Icons.play_circle_fill_rounded,
                      color: colorScheme.primary500,
                      size: 36,
                    ),
                    const SizedBox(width: 14),
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            state.isComplete
                                ? "Watch again"
                                : hasProgress
                                    ? "Continue watching"
                                    : "View your recap",
                            style: textTheme.largeBold,
                          ),
                          const SizedBox(height: 4),
                          Text(
                            subtitle,
                            style: textTheme.smallMuted,
                          ),
                        ],
                      ),
                    ),
                    const SizedBox(width: 8),
                    Icon(
                      Icons.chevron_right_rounded,
                      color: colorScheme.textMuted,
                    ),
                  ],
                ),
              ),
            ),
          ),
        ],
      ),
    );
  }

  void _handleTap(BuildContext context) {
    if (!wrappedStateService.shouldShowDiscoveryEntry &&
        !wrappedStateService.shouldShowHomeBanner) {
      return;
    }

    ScaffoldMessenger.of(context).showSnackBar(
      const SnackBar(
        content: Text("Wrapped viewer coming soon."),
        duration: Duration(seconds: 2),
      ),
    );
  }
}
